    private void ShowAvailRooms() {
        try {

            conn = DatabaseHelper.getDBConnection();
            stmt = conn.createStatement();
            String sql = "select room_no from room where book = 'Booked'";
            rs = stmt.executeQuery(sql);
            while (rs.next()) {
                roomno.addItem(rs.getString(1));
            }
            rs.close();
            stmt.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Sorry No Rooms Available");
        }
    }

    private void showAvailfood() {
        try {
            conn = DatabaseHelper.getDBConnection();
            stmt = conn.createStatement();
            rs = stmt.executeQuery("select item_name from foods where status='Available' ");
            while (rs.next()) {
                foodname.addItem(rs.getString(1));
                fn = rs.getString(1);

            }
            rs.close();
            stmt.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Sorry No Foods Available");
        } finally {
            try {
                rs.close();
                stmt.close();
                conn.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

