    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection conn = null;
        Statement sts = null;
        ResultSet rs = null;
        String rno = roomno.getSelectedItem().toString();
        String cnid = txtnid.getText();
        int c = 0;
        try {
            conn = DatabaseHelper.getDBConnection();
            sts = conn.createStatement();
            rs = sts.executeQuery("select * from reservation where Room_No='" + rno + "' and nid='" + cnid + "'");
            while (rs.next()) {
                c = rs.getRow();
            }
            rs.close();
            sts.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        if (txtnid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Please Enter NID Number");
            txtnid.requestFocus();
        } else {
            if (c == 0) {
                JOptionPane.showMessageDialog(rootPane, "Sorry You Dont Have permission to Order");
            } else {
                if (txtq.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(rootPane, "Please Food Quantity");
                    txtq.requestFocus();
                } else {
                    int rn = Integer.parseInt(roomno.getSelectedItem().toString());
                    int fq = Integer.parseInt(txtq.getText());
                    int fp = Integer.parseInt(txtp.getText());
                    String od = txtod.getText();
                    double total = fq * fp;
                    Connection con = null;
                    Statement st = null;
                    try {
                        con = DatabaseHelper.getDBConnection();
                        st = con.createStatement();
                        int count = 0;
                        count = st.executeUpdate("insert into bills (food_bill, room_no) values('" + total + "','" + rn + "')");
                        st.executeUpdate("update reservation set Food_bill='" + total + "', Food_date='" + od + "' where Room_No='" + rn + "'");
                        if (count > 0) {
                            
                            JOptionPane.showMessageDialog(null, "Successfully Ordered...!!");
                            RoomManager fpc = new RoomManager();
                            fpc.setVisible(true);
                            this.dispose();
                        }
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }

            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

